<h3><b><i>Basic Experimental Designs</i></b> </h3>
<p>Eleven commonly used experimental designs will be described. They include: </p>
<ol>
<li>One-Shot </li>
<li>One-Group, Pre-Post </li>
<li>Static Group </li>
<li>Random Group </li>
<li>Pre-Post Randomized Group </li>
<li>Solomon Four Group </li>
<li>Randomized Block </li>
<li>Factorial </li>
<li>One-Shot Repeated Measures </li>
<li>Randomized Groups Repeated Measures </li>
<li>Latin Square </li>
</ol>
<p>Obviously in a treatment of experimental design of fewer than 15
pages, not all possible designs are covered. Some of those not treated
include, incomplete block designs, Youden square designs, lattice
square designs, Taguchi designs, fractional factorial designs,
Graeco-Latin square designs, split-plot designs, covariance designs, or
time-series designs. The reader is referred to thgn, an intact group of subjects is given the treatment and
then measured or observed. No attempt is made to randomly assign
subjects to the groups, nor does the design provide for any additional
groups as comparisons. Thus, one group will be given one treatment and
one "observation." This design is diagrammed as follows: </p>
<blockquote>
<p><b>GP--T--O</b> </p>
