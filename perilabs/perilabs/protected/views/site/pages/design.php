<h1>Experiment Design Introduction</h1>
<p>By professor <i>Sid Sytsma</i>, 2006</p>
<p>Experimental design is a planned interference in the natural order
of events by the researcher. He does something more than carefully
observe what is occurring. This emphasis on experiment reflects the
higher regard generally given to information so derived. There is good
rationale for this. Much of the substantial gain in knowledge in all
sciences has come from actively manipulating or interfering with the
stream of events. There is more than just observation or measurement of
a natural event. A selected condition or a change (treatment) is
introduced. Observations or measurements are planned to illuminate the
effect of any change in conditions. </p>
<p>The importance of experimental design also stems from the quest for
inference about causes or relationships as opposed to simply
description. Researchers are rarely satisfied to simply describe the
events they observe. They want to make inferences about what produced,
contributed to, or caused events. To gain such information <b><i>without ambiguity</i></b>, some form of experimental design is 
ordinarily required. As a consequence, the need for using rather elaborate designs ensues from the possibility of alternative 
relationships, consequences or causes. The purpose of the design is to <b><i>rule out</i></b> these alternative causes, leaving only the actual 
factor that is the real cause. </p>
<p>For example, Treatment A may have caused observed Consequences O, but possibly the consequence may have derived from 
Event E instead of the treatment or from Event E combined with the treatment. It is this pursuit of clear and unambiguous 
relationships that leads to the need for carefully planned designs. </p>
<p>The kinds of planned manipulation and observation called
experimental design often seem to become a bit complicated. This is
unfortunate but necessary, if we wish to pursue the potentially
available information so the relationships investigated are clear and
unambiguous. </p>
<p>The plan that we choose to call a design is an essential part of research strategies. The design itself entails: </p>
<ul>
<li>selecting or assigning subjects to experimental units </li>
<li>selecting or assigning units for specific treatments or conditions of the experiment (experimental manipulation </li>
<li>specifying the order or arrangement of the treatment or treatments </li>
<li>specifying the sequence of observations or measurements to be taken </li>
</ul>
<p>By convention, the problems of design to not ordinarily include
details of sampling, selection of measurement instruments, selection of
the research problem or any other nuts and bolts of procedure required
to actually do the study. </p>
<h3><b><i>Considerations in Design Selection</i></b> </h3>
<p>The selection of a specific type of design depends primarily on both
the nature and the extent of the information we want to obtain. Complex
designs, usually involving a number of "control groups," offer more
information than a simple group design. If "more information per
project" were the sole criterion for selection of a design, we would be
led to more and more complex designs. However, not all of the relevant
information may be needed can be derived from any given design. Part of
the information will be piggy-backed into the study by assumptions,
some of which are explicit. Other information derives from a network of
knowledge surrounding the project in question. Theories, accepted
concepts, hypotheses, principles and empirical evidence from related
studies contribute. To the extent that this knowledge is already
available, the task of extracting the exact information needed to solve
any research problem is circumscribed. </p>
<p>Collecting information is costly. The money and staff resources
available have some limits. Subjects are usually found in finite
quantities only. Time is a major constraint. The information to be
gained has to be weighed against some estimate of the cost of
collection. This points ou two ways of checking potential designs: </p>
<ol>
<li><b>What questions will this design answer?</b> To do this, we must also be able to specify many of the questions the design 
won't answer as well ones it will answer. This should lead to a more realistic approach to experimental design than is usually 
given. Some simple and useful designs have been labeled as "poor" because they are relatively simple and will not answer 
some questions. Yet, they may provide clear and economical answers to the major questions of interest. Complex designs are 
not as useful for some purposes. </li>
<li><b>What is the relative information gain/cost picture?</b> There is no specific formula or strategy for deriving some cut-off point 
in this regard. The major point here is that the researcher must take a close look at the probable cost before selecting a 
design. </li>
</ol>
<h3><b><i>Experimental Design Terminology</i></b> </h3>
<p>The group in an experiment which receives the specified treatment is called the <b><i>Treatment Group</i></b> or the experimental group. 
However, the term <b><i>Control Group</i></b>
refers to another group assigned to the experiment, but not for the
purpose of being exposed to the treatment. Thus, the performance of the
control group usually serves as a baseline against which to measure the
effect of the full treatment on the treatment group. </p>
<p>A <b><i>variable</i></b> refers to almost anything under the sun.
There are only two kinds of stuff in the world for researchers:
variables and constants. As a result, almost any concept, or thing, or
event they are interested in, that varies or can be made to vary, and
that is related to their research can be called a variable. Researchers
pay particular attention to variables that may influence the results
(this is of MUCH concern to researchers). </p>
<p><b><i>Extraneous variables</i></b> (external to the experiment) are variables that may influence or affect the results of the treatment on the 
subject. </p>
<p>A variable of specific experimental interest is sometimes referred to as a <b><i>factor</i></b>.
Ordinarily the term factor. Ordinarily, the term is used when an
experiment involves more than one variable. These variables are often
identified as factors and are labeled "Factor A" and "Factor B," etc. <b><i>Level</i></b> refers to the degree or intensity of a factor. Any factor may be presented in one or more of several levels, 
including a zero level. </p>
<p><b><i>Randomness</i></b> refers to the property of completely chance
events that are not predictable (except in the sense that they are
random). If they are truly random, examining past instances of
occurrence should give the researcher no clues as to future
occurrences. Thus, if we were to predict outcome from perfect pairs of
dice rolled in an unbiased way (which are random events), previous
rolls give no clue. Randomness becomes important in the design of the
experiments primarily in the assignment of subjects to groups.
Researchers feel more secure about the results of their studies if
subjects have been randomly assigned to groups. <b><i>Random assignment</i></b>
of subjects to groups tends to spread out differences between subjects
in unsystematic (random) ways so that there is no tendency to give an
edge to any group. </p>
<p>Randomization, or random assignment, refers to a technique of
assignment or ordering such that no consistent or systematic effect in
the assignment is tied in with the method. Elimination of such
systematic influence upon assignment or selection allows for chance
assignment. Approved ways of generating chance assignments involve
tables of random numbers or the use of computer software with random
number generators. However, typically, researchers frequently resort to
simple counting off, flipping a coin, and other short cuts. </p>
<p>Another way of selecting subjects is simply to use intact groups:
such as all the students in a given classroom, or all of the patients
in a hospital. Researchers are usually worried whether the students
were assigned to the classroom in a non-random way, or whether certain
patients self-selected a hospital for a particular reason. The problem
is whether some subtle factors were operating to exert a bias of
selection factors in the assignment to groups. </p>
<p><b><i>Ex post facto</i></b> refers to causal inferences drawn "after
the fact." For in the ex post factor study, the causal event of
interest has already happened. These are known as non-experimental
studies and are often contrasted with experimental studies. A typical
example of this type of research would be to compare two groups of
patients in a hospital, one treated with Drug A and the other treated
with Drug B and then trying to infer a difference in the performance of
the two drugs. </p>
<p><b><i>Variance</i></b> refers to the variability of any event. If one uses a fine enough measuring device, one can find differences between any two 
objects or events. </p>
<p>The inside logic of an experiment is referred to as <b><i>internal validity</i></b>.
Primarily, it asks the question: Does it seem reasonable to assume that
the treatment has really produced the measured effect? Extraneous
variables which might have produced the effect with or without the
treatment are often called "threats to validity." </p>
<p><b><i>External validity</i></b>, on the other hand, refers to the proposed interpretation of the results of the study. If asks the question: With 
what other groups could we reasonably expect to get the same results if we used the same treatment? If Treatment X resulted in 
lowered blood pressure in middle age men, could you logically claim that it will produce the same effect in older women? </p>
<p><b><i>Blocks</i></b> usually refers to categories of subjects with a
treatment group. For example, we might divide the group into older,
middle aged, and younger patients and further divide the groups into a
group treated with Drug A and another treated with Drug B. The
advantage is to enable us to discover how the treatment affects each of
the age groups. For example, we might find that overall, Drug B out
performs Drug A, except for older patients, where Drug A out performs
Drug B. This phenomenon is known as an <i>interaction</i> between treatment (the Drug) and subject characteristics (age). </p>
<p>Interaction refers to variables in the treatment which may interact
with each other. It may make a difference whether a variable is used by
itself, with another, or with different levels or degrees of another.
Higher order interactions are possible. One factor may depend on the
presence or absence of two other factors; termed a second-order
interaction. </p>
<p>The <b><i>Hawthorne Effect</i></b> refers to the behavior of
interest being caused by subject being in the center of the
experimental stage, e.g., having a great deal of attention focused on
them. This usually manifests itself as a spurt or elevation in
performance or physical phenomenon measured. Although the Hawthorne
Effect is much more frequently seen in behavioral research, it is also
present in medical research when human subjects are present. Dealing
with this problem is handled by having a control group that is subject
to the same conditions as the treatment groups, then administering a
placebo to the control group. The study is termed a <b><i>blind</i></b><i> 
</i>experiment when the subject does not know whether he or she is receiving the treatment or a placebo. The study is termed <b><i>double 
blind</i></b> when neither the subject nor the person administering the treatment/placebo knows what is being administered knows either. </p>
<p>There are six major classes of information with which an experimental designer must cope. They include: </p>
<ul>
<li>post -treatment behavior or physical measurement <b>[P1]</b> </li>
<li>pre-treatment behavior or physical measurement <b>[P2]</b> </li>
<li>internal threats to validity<b> [I]</b> </li>
<li>comparable groups <b>[C]</b> </li>
<li>experiment errors <b>[E]</b> </li>
<li>relationship to treatment <b>[R]</b> </li>
</ul>
<p><b><i>Types of Data Yield</i></b> </p>
<p><b>[P1] </b><b><i>Post-Treatment Behavior or Physical Measurement</i></b> </p>
<p>In a typical experiment, this is the data, the class of information
of primary interest. What was the physical measurement or behavior of
the subject after treatment? All designs shed some light on this class
of information. Usually only immediate or short-range results are
obtained. More complicated kinds of information derive from, and
concern questions of comparing post-treatment behavior between groups
who have had various kinds, levels, or even absences of treatment. Five
categories of post-treatment behavior or physical measurement can be
identified: </p>
<ol>
<li>P1-1: behavior or measurement immediately after treatment </li>
<li>P1-2: a comparison of post-treatment behavior between experimental and control groups </li>
<li>P1-3: a comparison of the post-treatment behavior between experimental groups or blocks </li>
<li>P1-4: long-term effects with continuing treatment and periodic observations </li>
<li>P1-5: long-term effects without continuing treatment but with observation(s) </li>
</ol>
<p><b>[P2] </b><b><i>Pre-Treatment Behavior or Physical Measurement</i></b> </p>
<p>Information concerning pre-treatment behavior or condition requires
come observation, a test, or measurement, to be administered before the
experimental manipulation. Without such observations, the design itself
will not answer any questions about the subjects before the
experimental conditions have been introduced. Such information,
however, may be accrued from general knowledge or other studies. Direct
acquisition of this information adds to the cost of an experiment.
Furthermore, it may have a confounding effect, that is, sometimes the
pre-treatment observation or measurement influences the subsequent
behavior of the subject. When it is over, it may not be clear whether
the behavior was due to the treatment, the pre-treatment observation or
measurement, or both. Several classes of pre-treatment information can
be acquired: </p>
<ol>
<li>P2-1: behavior or measurement immediately before treatment </li>
<li>P2-2: comparing pre-treatment to post-treatment behavior or measurement </li>
<li>P2-3: a comparison of pre-treatment behavior or measurement between different pairs of subjects </li>
<li>P2-4: a comparison of the differences between pre-treatment and post-treatment behavior among groups of subjects </li>
<li>P2-5: the effect of the pre-treatment observation or measurement on subsequent behavior or measurement of the subject </li>
</ol>
<p><b>[I] </b><b><i>Internal Threats to Validity</i></b><b> </b></p>
<p>This class of information refers to some rival hypothesis that
threatens clear interpretation of the experiment. A common group of
rivals threatens most experiments, particularly those using human
subjects. Typically, the rival hypothesis asserts that something
outside of the experiment proper produced the behavior or measurement
of interest. To discover whether or not such rival events exert an
influence, the designer must usually provide for one or more control
groups. Typically, internal threats to validity include: </p>
<ol>
<li>I-1: the subjects exhibited behavior because of some event other than the treatment <ul>
<li>some other drug or process caused the change </li>
<li>the subject changed naturally [just improvedâ€¦] </li>
<li>the subject had a massive change in attitude or emotion </li>
<li>some other physical change occurred </li>
</ul>
</li>
<li>I-2: the subject could or would perform the behavior, or would have exhibited the measurement without the treatment </li>
</ol>
<p><b>[C] </b><b><i>Comparable Groups </i></b></p>
<p>This class of information, available only when two or more
experimental units or groups of subjects are used, deals with whether
the subjects in the different units were about the same in relevant
attributes before the treatment, and during the treatment, except for
the treatment condition itself. If the experimental designer cannot
provide information as to the comparability of groups, he/she must be
prepared to admit the possibility that the groups differed in some
essential aspect which produced the results observed. Equating the
groups by some pre-test or measurement or random assignment are the two
major techniques of providing this information. Thus, there are two
types of comparability information: </p>
<ol>
<li>C-1: were the groups (either experimental or control) comparable before the treatment? </li>
<li>C-2: did the groups receive a comparable degree of experiences during the time of the study (except for differences in 
treatment? </li>
</ol>
<p><b>[E] </b><b><i>Experiment Errors</i></b> </p>
<p>Experiment error refers to some unwanted side effect of the experiment itself which may be producing effect rather than the 
treatment. The Hawthorne Effect alluded to earlier is a continuing source of experimental error in both behavioral and medical 
research. Two types of strategies exist to deal with the Hawthorne effect. </p>
<ol>
<li>E-1: provide for a placebo treatment group which gets the attention, but not the "real" treatment and use blind and double 
blind strategies as needed </li>
<li>E-2: continue the treatment over a longer period of time; research shows that the Hawthorne effect tends to be short-lived </li>
</ol>
<p><b>[R] </b><b><i>Relationship to Treatment</i></b> </p>
<p>This class of information deals with the possible interaction of the
treatment effects with: different kinds of subjects, other treatments,
different factors within a complicated treatment, different degrees of
intensity, repeated applications or continuation of the treatment, and
different sequences or orders of the treatment or several treatments.
Typically, information of this type is acquired from blocking, from
factorial designs, and various repeated measures designs. </p>
<ol>
<li>R-1: did the treatment interact with subject characteristics so that subjects with different characteristics behaved or reacted 
differently? </li>
<li>R-2: how does the treatment interact when combined with other sorts of treatment? </li>
<li>R-3: does the treatment contain different factors which may operate differentially on the subjects? </li>
<li>R-4: what is the effect of different levels or degrees of the treatment? </li>
<li>R-5: what is the effect of different orders or sequences of various treatments? </li>
</ol>
