<p>The One-Shot Design is highly useful as an inexpensive measure of a
new treatment of the group in question. If there is some question as to
whether any expected effects will result from the treatment, then a
one-shot may be an economical route. In cases where other studies, or
the cumulative knowledge in the field provide information about either
pre-treatment baseline measurements or behavior, the effects of other
kinds of treatments, etc., the experimenter might sensibly decide that
it si not necessary to undertake a more extensive design. Simplicity,
ease, and low cost represent strong potential advantages in the
oft-despised one-shot. </p>
<p>This design answers only one question and that is in reference to
post-treatment behavior, P1-1. It will describe the information about
the behavior of the subjects shortly after treatment. </p>
<p><b>One-Group, Pre-Post</b> </p>
<p>In this design, one group is given a pre-treatment measurement or observation, the experimental treatment, and a post-treatment 
measurement or observation. The post-treatment measures are compared with their pre-treatment measures. This design is 
diagrammed as follows: </p>
<blockquote>
<p><b>GP--O--T--O</b> </p>
</blockquote>
<p>The usefulness of this design is similar to that of the one-shot,
except that an additional class of information is provided, i.e.,
pre-treatment condition or behavior. This design is frequently used in
clinical and education research to determine if changes occurred. It is
typically analyzed with a matched pairs t-test. </p>
