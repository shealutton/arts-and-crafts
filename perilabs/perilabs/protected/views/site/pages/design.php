interaction effects of the treatment. In a single 2x2 factorial design
similar to the one diagrammed above, information can be gained about
the effects of each of the two treatments and the effect of the two
levels within each treatment, and the interaction of the treatments. If
all these are questions of interest, the factorial design is much more
economical than running separate experiments. </p>
<p>The factorial handles some of the same classes of information as the
previously described randomized group design: P1-1, P1-3 and class C
questions. It provides little support for class I questions, but some
weak inferences might be drawn. The design also answers class R
questions, R-2, experimentation. </p>
<p><b>One-Shot Repeated Measures</b> </p>
<p>This design, or variations of it, is used to assess the effects of a
treatment with the same group or the same individual over a period of
time. A measure, or observation is made more than once to assess the
effects of the treatment. This design can be diagrammed as: </p>
<blockquote>
<p><b>GP--T--O--T--O--T--O</b> </p>
</blockquote>
<p>This design is an extension of the simple one-shot and adds only
information regarding the effects of repeated or continued treatment.
Often an economical trial balloon, the design can acquire high yield
when other extra-design sources of knowledge can be related to it. </p>
<p>This design handles only the questions related to class P1 or
post-treatment behavior, question P1-1 and P1-4. It answers questions
about behavior or effect shortly after treatment and the longer-term
effects related to subsequent treatments. It might handle class R
questions, R-5 relationships of the treatment in that the effect of the
repeated treatments may be observed. It will not answer questions P1-2,
P1-3, P1-5 nor any class P2, I, C, E, or R-1 through R-4 questions. </p>
<p><b>Randomized Groups Repeated Measures</b> </p>
<p>The Randomized Groups Repeated Measures design is a variant of the previous design in which two or more experimental methods 
are compared and repeatedly measured or observed. Although we are diagramming but two groups, more groups may be used. </p>
<blockquote>
<p><b>R--GP--T---O--T----O--T----O</b> </p>
<p><b>R--GP--T2--O--T2--O--T2--O</b> </p>
</blockquote>
<p>This design handles answers P1-1, P1-3, and P1-4 questions. It handles class C questions. The groups are comparable because of 
random assignment. Some light is shed upon the relationship of repeated treatments, question R-5. </p>
<p>It fails to handle P1-2, P1-5, or class P2 pre-treatment behavior or
measurement questions. It does not handle class I questions on internal
threats to validity unless a non-treatment group is added to yield
class I information. It further fails to handle istration may produce different results. A diagram of a three
treatment Latin Square design is: </p>
<blockquote>
<p><b>R--GP--TA--O--TB--O--TC--O </b></p>
<p><b>R--GP--TB--O--TC--O--TA--O </b></p>
</blockquote>
<blockquote>
<p><b>R--GP--TC--O--TA--O--TB--O </b></p>
</blockquote>
<p>If all possible sequencing permutations had to be addressed, there
would be 3x2=6 possible arrangements--a doubling of cost. The original
3-treatment design would, however, answer the question whether
sequencing made a difference without testing all possible sequences. </p>
<p>The Latin Square design answers questions P1-1, P1-3, C-1, C-2, and R-5. It will not provide information on P1-2, P1-4, P1-5 
nor any questions in class I or class E. Questions R-1 through R-4 are not addressed as well. </p>
<p>Sometimes in psychological, physiological or medical research,
individual subjects form the rows, time-the columns, and the cells form
the treatments with an observation taken at each treatment cell. When
this is done, and the measurements between each cell are to be
compared, clear interpretation may be difficult because of possible
carry-over effects of previous treatments (e.g., earlier treatments
affecting later ones). </p>
<p><b><i>The Question of External Validity</i></b> </p>
<p>Questions of a different sort than we have faced arise from our need
to generalize from a limited set of observations. No one is interested
in observations than in no way extend beyond this particular restricted
set of data. Generalizability depends on whether the observed behavior
measurement [O] is representative of the people, the surrounding
conditions and the treatments to which we now wish to extend it.
Classes of questions include: </p>
<ul>
<li>did some of the early procedure in the research affect the subjects so that their later measurements were, in part a result of 
that? </li>
<li>were the subjects themselves a representative sample of the general population of people to which it is desired to extend the 
research findings? </li>
<li>was there something in the research or selved? </li>
</ul>
<p>The important thing is to clarify where the results of your
observations may be legitimately extended and where they can not yet be
legitimately extended. Helpful in this regard is a comprehensive
description of the demographic characteristics of the subjects of the
research and a complete and comprehensive description of the
methodology used so that the reader of the research can judge for
himself or herself whether the results can be generalized to his or her
situation. </p>
