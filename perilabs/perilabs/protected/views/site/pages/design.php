<p><b>GP--T--O</b> </p>
<p><b>GP------O</b> </p>
</blockquote>
<p>This design may provide information on some rival hypotheses.
Whether it does or not depends on the initial comparability of the two
groups and whether their experience during the experiment differs in
relevant ways only by the treatment itself. </p>
<p>Whether the groups were comparable or not is crucial in determining
the extent of information yielded by this design. The design is could
be used to compare the value of a drug. If the designer cannot, on the
basis of information outside the experiment itself, assume the
comparability of the groups, the design will yield only information
regarding P1-1 and P1-2. However, IF additional information is
available to equate the two groups initially, it may handle some of the
class I questions. Without additional information, on the basis of the
design alone, it cannot. </p>
<p><b>Random Group</b> </p>
<p>This design is similar to the Static Group design except than an
attempt is made to insure similarity of the groups before treatment
begins. Since it is difficult to have exactly similar subjects in each
of two groups (unless you separate identical twins), the design works
toward a guarantee of comparability between groups by assigning
subjects to groups at random. If the researcher does this there is
likely to be reasonable comparability between the two groups. This
design can be diagrammed as: </p>
<blockquote>
<p><b>R--GP--T--O</b> </p>
<p><b>R--GP------O</b> </p>
</blockquote>
<p>This design is a real workhorse. It was developed by the great
statistician, Sir Ronald Fisher as part of his work in agricultural
statistics and has been a primary experimental design for more than 100
years. It is economical. It provides fairly clear-cut information as to
the relationship between treatment and post-treatment measurement or
behaviors. Since this is often the sole reason for the research, the
randomized group design is frequently the appropriate selection. </p>
<p>Good that it is, it does not provide information about pre-treatment
behavior. This design will answer questions P1-1 and P1-2. Since the
groups are randomized, the design will cope with the internal threats
to validity. Subject changes due to other causes should affect the
control group so that a comparison of the post-treatment behavior
should reveal any differential effects of the treatment. </p>
<p>Class C, comparable group questions C-1 and C-2 are also answered in
the randomization provided that there are no probable differences
between the groups entering the experiment except for the treatment.
The design will not cope with class P1-3, P1-4, P1-5, P2's questionss
of pre-treatment behavior, class E questions, or class R questions. It
is frequently analyzed with a two-sample t-test assuming equal
variances of the groups. </p>
<p><b>Pre-Post Randomized Group</b> </p>
<p>This design adds a pre-test to the previous design as a check on the
degree of comparability of the control and experimental groups before
the treatment is given. This experimental design could be diagrammed
as: </p>
<blockquote>
<p><b>R--GP--O--T--O</b> </p>
<p><b>R--GP--O------O</b> </p>
</blockquote>
<p>This yields information of P1-1, P1-2 as to post-treatment behavior
and a comparison of post-treatment behavior between groups. It also
answers must P2 questions on pre-treatment behavior, and questions P2-1
through P2-4. It answers most of the class I questions, that is,
threats to internal validity. It handles class C questions, the groups
are comparable because thar are randomized. </p>
<p>The design does not answer P1-3, P1-4, or P1-5; nor the class E
questions relating to experiment errors. It does not answer the class R
questions regarding the relationship of the treatment nor P2-5, the
effect of the pre-treatment observations on the subsequent behavior or
measurements of the subjects. </p>
<p><b>Solomon Four Group</b> </p>
<p>The Solomon Four Group design attempts to control for the possible
"sensitizing" effects of the pre-test or measurement by adding two
groups who have not been a part of the pre-test or pre-measuremen-3.
It answers the P2 pre-treatment questions, including the effect of the
pre-treatment measurement process. It handles threats to internal
validity, the class I questions. It also handles the class C questions
because the groups are randomized. It does not handle P1-4, P1-5, the
class E questions nor the class R questions. </p>
<p><b>Randomized Block</b> </p>
<p>This design is of particular value when the experimenter wishes to
determine the effect of a treatment on different types of subjects
within a group. This design can be diagrammed as: </p>
<blockquote>
<p><b>----------BLK--T--O</b> </p>
<p><b>R--GP</b> </p>
<p><b>----------BLK--T--O</b> </p>
<p><b>----------BLK------O</b> </p>
<p><b>R--GP</b> </p>
<p><b>----------BLK------O</b> </p>
</blockquote>
<p>Typically, this design refers to blocking or grouping of subjects
with similar characteristics into treatment subgroups. The group to be
used in an experiment is usually given some pre-treatment measure, or
previous records are examined, and the entire group is blocked or
sorted into categories. Then equal numbers from each category are
assigned to the various treatment and/or control groups. </p>
<p>While blocking according to subject characteristics is most typical
of this design, blocking could be based on other relevant attributes.
For example, if subjects are to be treated during different times of
the day, such as morning and afternoon, we might block a morning and an
afternoon group within each treatment condition. </p>
<p>The importance of this design lies in the probability that the
variable upon which the blocking is based may interact with the
treatment. Frequently, no overall treatment effect is observed because
subjects with different characteristics react differentially to
treatment. If they were blocked on the appropriate attributes,
differential treatment effects would be revealed. </p>
